    private void rellenarCampos() {
        jLabelTelefono.setVisible(false);
        jtfTelefono.setVisible(false);
        jtfDNI.setText(clienteActual.getDni());
        jtfDNI.setEditable(false);
        jtfDNI.setEnabled(false);
        jtfNombre.setText(clienteActual.getNombre());
        jtfApellido1.setText(clienteActual.getApellido1());
        jtfApellido2.setText(clienteActual.getApellido2());
    }
   
