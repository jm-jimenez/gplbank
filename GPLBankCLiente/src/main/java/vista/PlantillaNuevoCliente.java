    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        boolean datosRellenos = VistaUtils.getInstance().comprobarJtfRellena(jtfDNI);
        datosRellenos = VistaUtils.getInstance().comprobarJtfRellena(jtfNombre) && datosRellenos;
        datosRellenos = VistaUtils.getInstance().comprobarJtfRellena(jtfApellido1) && datosRellenos;

        if (datosRellenos) {

            String dni = jtfDNI.getText();
            String nombre = jtfNombre.getText();
            String apellido1 = jtfApellido1.getText();
            String apellido2 = jtfApellido2.getText();
            TreeSet<String> telefonos = new TreeSet<>();
            if (!jtfTelefono.getText().equals("")) {
                telefonos.add(jtfTelefono.getText());
            }

            Cliente c = new Cliente(nombre, apellido1, apellido2, dni, "", telefonos);
            if (!modificar) {
                Info info = controlClientes.addCliente(c);
                if (info.isSuccess()) {
                    controlEmpleados.anotarClienteCaptado(parent.getEmpleadoActivo());
                    JOptionPane.showMessageDialog(this, info.getMsg(), "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, info.getMsg(), "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                Info info = controlClientes.modificarCliente(c);
                if (info.isSuccess()) {
                    productosContratadosModel.refresh();
                    clienteActual = c;
                    ((DefaultTableModel) parent.getTablaDatosCliente().getModel()).removeRow(0);
                    ((DefaultTableModel) parent.getTablaDatosCliente().getModel()).addRow(new Object[]{clienteActual.getDni(), clienteActual.getNombre(), clienteActual.getApellido1(), clienteActual.getApellido2()});
                    JOptionPane.showMessageDialog(this, info.getMsg(), "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, info.getMsg(), "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Rellene los campos", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


